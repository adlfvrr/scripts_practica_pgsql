CREATE TABLE TERCEROS (
	ID_TERCERO SERIAL,
	NOMBRE CHARACTER VARYING(70) NOT NULL,
	CUITL CHARACTER VARYING(15) NOT NULL,
	SITIVA CHARACTER VARYING(50) NOT NULL,
	DIRECCION CHARACTER VARYING(70) NOT NULL,
	LOCALIDAD CHARACTER VARYING(70),
	PROVINCIA CHARACTER VARYING(20),
	TELEFONOS CHARACTER VARYING(120),
	SALDO_APERTURA NUMERIC(10,2),
	TIPO_SALDO CHARACTER VARYING(8),
	CONSTRAINT TERCEROS_PK PRIMARY KEY (ID_TERCERO)
);

CREATE TABLE FACTURAS (
	ID_FACTURA SERIAL,
	FECHA_FACTURA DATE NOT NULL,
	ID_TERCERO INTEGER NOT NULL,
	NUMERO INTEGER,
	CONSTRAINT FACTURAS_PK PRIMARY KEY (ID_FACTURA),
	CONSTRAINT FACTURAS_TERCEROS_FK FOREIGN KEY (ID_TERCERO) REFERENCES TERCEROS(ID_TERCERO) ON DELETE SET NULL ON UPDATE CASCADE
);

CREATE TABLE FACTURAS_ITEMS (
	ID_ITEMS SERIAL,
	MONTO NUMERIC(8,2) NOT NULL,
	CANTIDAD NUMERIC(9,3) NOT NULL,
	ID_FACTURA INTEGER NOT NULL,
	DETALLE CHARACTER VARYING(300),
	CONSTRAINT FACTURAS_ITEMS_PK PRIMARY KEY (ID_ITEMS),
	CONSTRAINT FACTURAS_ITEMS_FACTURAS_FK FOREIGN KEY (ID_FACTURA) REFERENCES FACTURAS(ID_FACTURA) ON DELETE CASCADE
);

CREATE TABLE FACULTAD (
	ID_FACULTAD SERIAL,
	NOMBRE CHARACTER VARYING(70) NOT NULL,
	DIRECCION CHARACTER VARYING(100) NOT NULL,
	CUIT CHARACTER VARYING(15) NOT NULL,
	SUCURSAL SMALLINT NOT NULL,
	TELEFONOS CHARACTER VARYING(120),
	EMAIL CHARACTER VARYING(70),
	DEFECTO BOOLEAN DEFAULT FALSE,
	CONSTRAINT FACULTAD_PK PRIMARY KEY (ID_FACULTAD)
);

CREATE TABLE PAGOS (
	ID_PAGOS SERIAL,
	ID_TERCERO INTEGER NOT NULL,
	FECHA_PAGO DATE NOT NULL,
	MONTO_PAGO NUMERIC(12,2),
	MODO_PAGO CHARACTER VARYING(20) NOT NULL,
	CONSTRAINT PAGOS_PK PRIMARY KEY (ID_PAGOS),
	CONSTRAINT PAGOS_TERCEROS_FK FOREIGN KEY (ID_TERCERO) REFERENCES TERCEROS(ID_TERCERO) ON DELETE SET NULL ON UPDATE CASCADE
);

CREATE TABLE PAGOS_DETALLE (
	ID_PAGOSDETALLE SERIAL,
	INSTRUMENTNUMBER CHARACTER VARYING(15) NOT NULL,
	INSTRUMENTDATE DATE NOT NULL,
	BANCO CHARACTER VARYING(100),
	PAGOREALIZADO BOOLEAN DEFAULT False,
	ID_PAGOS INTEGER NOT NULL,
	CONSTRAINT PAGOS_DETALLE_PK PRIMARY KEY (ID_PAGOSDETALLE),
	CONSTRAINT PAGOS_DETALLE_PAGOS_FK FOREIGN KEY (ID_PAGOS) REFERENCES PAGOS(ID_PAGOS) ON DELETE SET NULL ON UPDATE CASCADE
);